export type TaskBase = {
  id: string;
  prompt: string;
  hint: string;
  solution: string[];
};


export type SingleTask = TaskBase & {
  type: 'single';
  options: string[];
  answer: number;
};

export type MultipleTask = TaskBase & {
  type: 'multiple';
  options: string[];
  answer: number[];
};

export type NumberTask = TaskBase & {
  type: 'number';
  answer: number;
  tol?: number;
  unit?: string;
};

export type Task = SingleTask | MultipleTask | NumberTask;

export interface Topic {
  id: string;
  name: string;
  blurb: string;
  tasks?: Task[];
  locked?: boolean;
  section?: string;
  notebook?: boolean;
}

export interface Subject {
  id: string;
  name: string;
  glyph: string;
  tint: string;
  blurb: string;
  topics: Topic[];
}

